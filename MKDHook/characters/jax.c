void init_jax_toc()
{
	init_jax_patches();

	int baseSize = 2048;
	for (int i = 0; i < JAX_FILES; i++)
	{
		jax_entry_table[i].belong = &jax_file_table[0];
	}


	for (int i = 1; i < JAX_FILES; i++)
	{
		jax_file_table[i].previousSize = baseSize;
		baseSize += (jax_file_table[i].size + 2048 - 1) & -2048;
	}

	// alt

	baseSize = 2048;

	for (int i = 0; i < JAX_FILES; i++)
	{
		jax_alt_entry_table[i].belong = &jax_alt_file_table[0];
	}


	for (int i = 1; i < JAX_FILES; i++)
	{
		jax_alt_file_table[i].previousSize = baseSize;
		baseSize += (jax_alt_file_table[i].size + 2048 - 1) & -2048;
	}
}

void init_jax_patches()
{
	int sb_data = (int)&blast_effect_at_plyr_jax[0];
	int pb_data = (int)&p_blast_jax[0];

	patchInt((int)&p_blast_jax[0x180], jal((int)jax_blast_color_patch));

	patchInt((int)&blast_effect_at_plyr_jax[0x8C], lui(a2, HIWORD(pb_data)));
	patchInt((int)&blast_effect_at_plyr_jax[0x98], ori(a2, a2, LOWORD(pb_data)));

	patchInt((int)&blast_effect_at_plyr_jax[0x84], jal((int)jax_blast_position_patch));

}

int jax_grab_aux_weapon(int a1, int a2)
{
	int aux_weapon = plyr_aux_weapon_release(a1);

	if (aux_weapon)
	{
		*(int*)(aux_weapon + 92) = a2;
		plyr_aux_weapon_grab(a1, aux_weapon);
	}
	return aux_weapon;
}


